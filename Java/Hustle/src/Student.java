public class Student{
    int time_of_living;
    String name;
    double gpa;
    int year;
    int month;

    //Construction
    Student (int age, String name,double gpa,int year,int month){
        this.time_of_living= age;
        this.name= name;
        this.gpa = gpa;
        this.year= year;
        this.month = month;
    }
}